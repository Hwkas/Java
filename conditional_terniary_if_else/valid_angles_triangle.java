import java.util.Scanner;

class ValidAnglesTriangle{

	public static void main(String [] args){

		Scanner s = new Scanner(System.in);

		System.out.print("Enter the angle of triangle you want to check(separated by space): ");

		int a = s.nextInt();
		int b = s.nextInt();
		int c = s.nextInt();

		// using if else

		if (a+b+c == 180 && a != 0 && b != 0 && c != 0)
			System.out.println("This triangle is valid.");
		else System.out.println("This triangle is invalid.");

		//

		// using terniary operator

		System.out.println((a+b+c == 180 && a != 0 && b != 0 && c != 0)?"This triangle is valid.":"This triangle is invalid.");
		
		//
	}

}
